class I18n {
    constructor() {
        this.currentLang = localStorage.getItem('app_lang') || 'zh';
        this.translations = translations;
    }

    setLanguage(lang) {
        if (!this.translations[lang]) return;
        this.currentLang = lang;
        localStorage.setItem('app_lang', lang);
        this.applyTranslations();
        this.updateActiveButton();
        
        // Dispatch event for other components
        window.dispatchEvent(new CustomEvent('languageChanged', { detail: lang }));
    }

    get(key, params = {}) {
        let text = this.translations[this.currentLang][key];
        // Fallback to English if key missing in current lang (except for zh root)
        if (!text && this.currentLang !== 'en') {
             text = this.translations['en'][key];
        }
        // Fallback to key if missing everywhere
        if (!text) return key;

        // Simple replacement for {param}
        for (const [k, v] of Object.entries(params)) {
            text = text.replace(`{${k}}`, v);
        }
        return text;
    }

    applyTranslations() {
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(el => {
            const key = el.getAttribute('data-i18n');
            const text = this.get(key);
            if (text && text !== key) {
                const count = el.getAttribute('data-count'); // Handle count params if any
                if (count) {
                    el.textContent = this.get(key, { count });
                } else {
                    // Preserve HTML structure if needed? Usually textContent is safer.
                    // But some descriptions might need <br>.
                    // For now textContent.
                    el.textContent = text;
                }
            }
        });
    }

    updateActiveButton() {
        const btns = document.querySelectorAll('.lang-btn');
        btns.forEach(btn => {
            if (btn.dataset.lang === this.currentLang) {
                btn.classList.add('active');
            } else {
                btn.classList.remove('active');
            }
        });
    }
}

// Initialize global i18n instance
window.i18n = new I18n();

// Auto-apply on load
document.addEventListener('DOMContentLoaded', () => {
    window.i18n.applyTranslations();
    window.i18n.updateActiveButton();
});
